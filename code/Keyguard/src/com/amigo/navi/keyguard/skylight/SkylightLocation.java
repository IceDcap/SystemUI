package com.amigo.navi.keyguard.skylight;

public class SkylightLocation {

	private int xaxis = 0;
	private int yaxis = 0;
	private int width = 0;
	private int height = 0;



	public int getXaxis() {
		return xaxis;
	}

	public void setXaxis(int xaxis) {
		this.xaxis = xaxis;
	}

	public int getYaxis() {
		return yaxis;
	}

	public void setYaxis(int yaxis) {
		this.yaxis = yaxis;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}
	
	

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public void reset() {
		xaxis = 0;
		yaxis = 0;
		width = 0;
		height = 0;
	}
	
	@Override
	public String toString() {
		return "xaxis:  "+xaxis+"  yaxis: "+yaxis+"width: "+width+"  height: "+height;
	}

}
